using UnityEngine;
using System.Collections;
using UnityEngine.UI;

public class ScoreComparator : MonoBehaviour {

	public Text scoreP1;
	public Text scoreP2;

	// Use this for initialization
	void Start () {
		
	}
	
	// Update is called once per frame
	void Update () {
		float s1 = float.Parse (scoreP1.text);
		float s2 = float.Parse (scoreP2.text);

		if (s1 > s2) {
			scoreP1.color = Color.Lerp (Color.white, Color.green, 0.75f);
		} else if (s1 < s2) {
			scoreP1.color = Color.Lerp (Color.white, Color.red, 0.75f);
		} else {
			scoreP1.color = Color.white;
		}
	}
}
